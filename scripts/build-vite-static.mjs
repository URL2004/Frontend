import { build } from 'vite';
import fs from 'node:fs/promises';
import path from 'node:path';

const root = process.cwd();
const dist = path.join(root, 'dist');
const viteScratch = path.join(root, '.vite-static-build');
const viteOut = path.join(root, '.vite-static-out');
const staticEntries = [
  'index.html',
  'assets',
  'pages',
  'partials',
  'favicon.png',
  'og-image.png',
  'robots.txt',
  'sitemap.xml',
  'CNAME',
  'vercel.json'
];

async function exists(p) {
  try {
    await fs.access(p);
    return true;
  } catch {
    return false;
  }
}

async function copyIfExists(entry) {
  const src = path.join(root, entry);
  if (!(await exists(src))) return;
  await fs.cp(src, path.join(dist, entry), { recursive: true, force: true });
}

function literal(value) {
  return JSON.stringify(value == null ? '' : String(value));
}

async function writeRuntimeConfig() {
  const rawFirebase = process.env.VITE_FIREBASE_CONFIG_JSON || '';
  let firebaseLine = '';
  if (rawFirebase.trim()) {
    try {
      firebaseLine = `,\n  FIREBASE: ${JSON.stringify(JSON.parse(rawFirebase))}`;
    } catch (err) {
      throw new Error(`VITE_FIREBASE_CONFIG_JSON is not valid JSON: ${err.message}`);
    }
  }

  const body = `// Generated by scripts/build-vite-static.mjs during Vite build.
window.APP_RUNTIME_CONFIG = {
  APP_ENV: ${literal(process.env.VITE_APP_ENV || 'preview')},
  SITE_URL: ${literal(process.env.VITE_SITE_URL || '')},
  API_BASE: ${literal(process.env.VITE_API_BASE || '')},
  TOSS_CLIENT_KEY: ${literal(process.env.VITE_TOSS_CLIENT_KEY || '')},
  KAKAO_INQUIRY_URL: ${literal(process.env.VITE_KAKAO_INQUIRY_URL || 'https://open.kakao.com/o/s3Jegizi')}${firebaseLine}
};
`;
  await fs.writeFile(path.join(dist, 'runtime-config.js'), body, 'utf8');
}

await fs.rm(dist, { recursive: true, force: true });
await fs.rm(viteScratch, { recursive: true, force: true });
await fs.rm(viteOut, { recursive: true, force: true });

// Use Vite for the build lifecycle while preserving the legacy app's absolute
// /assets, /pages, and /partials layout during migration. Building the real
// index.html would bundle module scripts and double-initialize the legacy app.
await fs.mkdir(viteScratch, { recursive: true });
await fs.writeFile(path.join(viteScratch, 'index.html'), '<div id="vite-static-build"></div><script type="module" src="/entry.js"></script>\n', 'utf8');
await fs.writeFile(path.join(viteScratch, 'entry.js'), 'export default {};\n', 'utf8');
await build({
  root: viteScratch,
  publicDir: false,
  build: {
    outDir: viteOut,
    emptyOutDir: true
  }
});

await fs.rm(viteScratch, { recursive: true, force: true });
await fs.rm(viteOut, { recursive: true, force: true });
await fs.mkdir(dist, { recursive: true });

for (const entry of staticEntries) {
  await copyIfExists(entry);
}

await writeRuntimeConfig();
